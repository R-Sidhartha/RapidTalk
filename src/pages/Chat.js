import React, { useEffect, useState, useRef } from "react";
import axios from "axios";
import { useNavigate } from "react-router-dom";
import { io } from "socket.io-client";
import { allUsersRoute, host } from "../utils/APIRoutes";
import ChatContainer from "../components/ChatContainer";
import Contacts from "../components/Contacts";
import Welcome from "../components/Welcome";

export default function Chat() {
  const navigate = useNavigate();
  const socket = useRef();
  const [contacts, setContacts] = useState([]);
  const [currentChat, setCurrentChat] = useState(undefined);
  const [currentUser, setCurrentUser] = useState(undefined);
  const [isContactSelected,setisContactSelected] = useState(false);
  useEffect(async () => {
    if (!localStorage.getItem(process.env.REACT_APP_LOCALHOST_KEY)) {
      navigate("/login");
    } else {
      setCurrentUser(
        await JSON.parse(
          localStorage.getItem(process.env.REACT_APP_LOCALHOST_KEY)
        )
      );
    }
  }, []);
  useEffect(() => {
    if (currentUser) {
      socket.current = io(host);
      socket.current.emit("add-user", currentUser._id);
    }
  }, [currentUser]);

  useEffect(async () => {
    if (currentUser) {
      if (currentUser.isAvatarImageSet) {
        const data = await axios.get(`${allUsersRoute}/${currentUser._id}`);
        setContacts(data.data);
      } else {
        navigate("/setAvatar");
      }
    }
  }, [currentUser]);
  const handleChatChange = (chat) => {
    setCurrentChat(chat);
  };
  return (
    <>
      <div className="flex justify-center items-center h-screen w-screen bg-gray-700">
        <div className="w-full h-full bg-black bg-opacity-60 flex ">
          <Contacts contacts={contacts} changeChat={handleChatChange} isContactSelected={isContactSelected} setisContactSelected={setisContactSelected}/>
          {currentChat === undefined ? (
            <Welcome isContactSelected={isContactSelected} setisContactSelected={setisContactSelected}/>
          ) : (
            <ChatContainer currentChat={currentChat} socket={socket} isContactSelected={isContactSelected} setisContactSelected={setisContactSelected}/>
          )}
        </div>
      </div>
    </>
  );
}